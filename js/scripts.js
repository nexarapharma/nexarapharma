// 获取语言
function getLanguage() {
    const urlParams = new URLSearchParams(window.location.search);
    const urlLang = urlParams.get("lang");
    const storedLang = localStorage.getItem('language');
    return urlLang || storedLang || 'zh';
}

// 应用语言
function applyLanguage(lang) {
    document.title = translations[lang].title;

    const ids = [
        "title", "home", "about", "company-profile", "vision", "team",
        "technology", "pipeline", "pipeline-overview", "pipeline-item",
        "news", "news-home", "news-post", "careers", "contact"
    ];
    ids.forEach(id => {
        const element = document.getElementById(id);
        if (element) {
            element.textContent = translations[lang][id];
        }
    });

    document.querySelectorAll('[data-lang]').forEach(element => {
        const key = element.getAttribute('data-lang');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    localStorage.setItem('language', lang);

document.querySelectorAll('[data-lang-placeholder]').forEach(element => {
    const key = element.getAttribute('data-lang-placeholder');
    if (translations[lang][key]) {
        element.setAttribute('placeholder', translations[lang][key]);
    }
});
}

// 平滑滚动到目标，支持 id 和 data-alt-id
function scrollToTarget(hash) {
    let target = document.getElementById(hash);
    if (!target) {
        target = document.querySelector(`[data-alt-id="${hash}"]`);
    }
    if (target) {
        const offset = 80; // 导航栏高度
        const targetPosition = target.getBoundingClientRect().top + window.pageYOffset - offset;
        window.scrollTo({
            top: targetPosition,
            behavior: 'smooth'
        });
    } else {
        console.log("Target not found:", hash);
    }
}

// 加载 header 并处理所有锚点跳转
fetch('header.html')
    .then(response => response.text())
    .then(data => {
        document.getElementById('header-placeholder').innerHTML = data;
        const lang = getLanguage();
        console.log("Applying language:", lang);
        applyLanguage(lang);

        // 处理所有带有 href="#..." 的链接
        document.querySelectorAll('a[href*="#"]').forEach(link => {
            link.addEventListener('click', function (e) {
                const href = this.getAttribute('href');
                const [page, hash] = href.split('#');

                if (hash) {
                    e.preventDefault();
                    const currentPage = window.location.pathname.split('/').pop() || 'index.html';

                    if (!page || page === currentPage) { // 页面内跳转
                        scrollToTarget(hash);
                        window.history.pushState({}, document.title, '#' + hash);
                    } else { // 跨页面跳转
                        window.location.href = href;
                    }
                }
            });
        });

        // 初始化时检查 URL 中的锚点
        if (window.location.hash) {
            scrollToTarget(window.location.hash.substring(1));
        }
    })
    .catch(error => console.error('Error loading header:', error));

// 监听 hash 变化
window.addEventListener('hashchange', () => {
    if (window.location.hash) {
        scrollToTarget(window.location.hash.substring(1));
    }
});

